package com.youyuan.day05;

public class Animal {
    int age;
    String name ;
    public void eat (){
        System.out.println("动物吃饭");
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }
}


